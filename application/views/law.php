                                18. هرگونه پیام یا مطلبى از جمله آگهی، عکس، پیام در تماس با ما و غیر آن که به سایت مکان با ما ارسال مى‌کنید غیر محرمانه و غیر قابل تملک تلقى خواهد شد. هر چیزى را که ارسال نمایید مى‌تواند توسط سایت مکان با ما به هر منظورى، شامل ولى غیر محدود به، باز تولید، افشاءِ، مخابره، نشر، پخش و یا ارسال و الصاق بکار رود.
                            <p class="paragraph">
                                19. سایت مکان با ما، سرویس حاضر را به همان شکلی که هست و بدون هرگونه تعهدی ارائه می کند. سایت مکان با ما در مورد عدم قطع شدن سایت، خالی از اشکال بودن آن، رفع اشکالات یا تطابق آن با نیازهای شما تعهدی ندارد.
                            <p class="paragraph">
                                20. سایت مکان با ما، جهت نمایش آگهی‌ها چه به صورت عادی و چه به صورت ویژه، قرارداد و یا تعهد نامه‌ای با آگهی دهندگان منعقد نمی‌کند. مطالب مندرج در آگهی‌ها ممکن است توسط آگهی دهندگان بصورت مداوم تغییر کند و یا به روز شود.
